def parse(str):
    """
    Функция вычисляет количество пробелов в строке
    Входные параметры: строка
    Возвращаемое значение: количество пробелов в строке
    """
    count = 0
    for i in range(0, len(str)):
        if str[i] == " ":
            count += 1
    return len(str) - count
