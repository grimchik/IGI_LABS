    """
    Функция для выполнения 3 задания. Запрашивается на ввод строка.
    Вызывается функция для подсчета количества пробелов.
    Результат выводится на экран.
    Параметры: нет
    Возвращает: Ничего
    """
    str = input("Input string: ")
    print(f"Count of non-space symbols: {parse(str)}")